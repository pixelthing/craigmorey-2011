<p>A site on the "for a friend" ticket that all designers occasionally get. No matter how small a business, having a decent website is one of the best marketing tools you can have to get a head start. It didn't hurt that I'd worked with my friend for years and he was a top-notch print designer with a marketing background.</p>

<p>Web teams everywhere have gone through a transition in the last few years. Where we once received designs from print designers who asked what DPI was needed and why the font wasn't exactly anti-aliased as the Illustrator file was, now we get designs that take into the account the context of the web browser, and the latitude to make a flat design into a living website.</p>

<p>The White Nuptials site is an example of building a relationship between a developer and a designer so that the final result is the best qualities of both. The process of educating each other led to the designer having a clear idea of what was possible and what worked, and for my part, I tried to not just say "that won't work", and to strive to keep as close as possible to the original creative idea, while suggesting new twists. With a design led project like this, it's the details that can let it down, and a finished article that looks "pretty much" like the original concept is often it's downfall.</p>

