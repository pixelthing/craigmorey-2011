<p>Favelets are browser bookmarks that run scripts on the page you're viewing, usually to reveal some of the inner workings of a web page to help in it's construction. "Honey I shrunk the page" (HISTP) was a favelet that I built to scratch an itch of my own. I was building a web-app that constantly had elements sliding out of view, and I didn't have a clear view of what they were doing after they disappeared offscreen.</p>

<p>It occurred to me that you could use CSS transforms to scale the entire viewport, in effect shrinking the whole visible browser window down so that you could see the all the hidden areas as well. Using that technique I built this favelet, and wanted to make it work in a non-buggy, polished way (there are plenty of those). I was chuffed with the result - it might be a tiny tool that was made for an edge-case use, but it works like a charm and helped me no-end with my work.</p>

